CREATE TABLE APPLE(
	PRODUCT_TYPE VARCHAR2(30) NOT NULL,
	PRODUCT_MODEL VARCHAR2(30) NOT NULL,
	PRODUCT_MEMORY VARCHAR2(20),
	PRODUCT_CORLOR VARCHAR2(30) NOT NULL,
	PRODUCT_PRICE NUMBER,
	APPLE_CARE CHAR(1) DEFAULT 'N' CHECK( APPLE_CARE IN ('Y', 'N') )
);

COMMENT ON COLUMN APPLE.PRODUCT_TYPE IS '상품타입';
COMMENT ON COLUMN APPLE.PRODUCT_MODEL IS '모델명';
COMMENT ON COLUMN APPLE.PRODUCT_MEMORY IS '저장용량';
COMMENT ON COLUMN APPLE.PRODUCT_CORLOR IS '색상';
COMMENT ON COLUMN APPLE.PRODUCT_PRICE IS '가격';
COMMENT ON COLUMN APPLE.APPLE_CARE IS '애플케어SV';


